export function getJobsByCategory(category: string): Job[] {
  // Simple mock filter logic
  const lowerCat = category.toLowerCase();
  
  if (lowerCat === "remote") return jobsData.filter(j => j.location.toLowerCase() === "remote");
  if (lowerCat === "lahore") return jobsData.filter(j => j.location.toLowerCase() === "lahore");
  if (lowerCat === "karachi") return jobsData.filter(j => j.location.toLowerCase() === "karachi");
  if (lowerCat === "islamabad") return jobsData.filter(j => j.location.toLowerCase() === "islamabad");
  if (lowerCat.includes("react")) return jobsData.filter(j => j.skills.some(s => s.toLowerCase() === "react"));
  if (lowerCat.includes("node")) return jobsData.filter(j => j.skills.some(s => s.toLowerCase().includes("node.js")));
  if (lowerCat.includes("mern")) return jobsData.filter(j => j.skills.some(s => ["mongodb", "express", "react", "node.js"].includes(s.toLowerCase())));
  if (lowerCat.includes("ai")) return jobsData.filter(j => j.skills.some(s => s.toLowerCase() === "python"));
  if (lowerCat.includes("intern")) return jobsData.filter(j => j.type.toLowerCase() === "internship");
  if (lowerCat.includes("devops")) return jobsData.filter(j => j.skills.some(s => s.toLowerCase() === "docker" || s.toLowerCase() === "kubernetes"));
  
  return jobsData;
}
